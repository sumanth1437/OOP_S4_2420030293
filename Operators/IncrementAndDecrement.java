package Operators;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		int x = 5;

		System.out.println("x++ = " + (x++));
        System.out.println("++x = " + (++x));
        System.out.println("x-- = " + (x--));
        System.out.println("--x = " + (--x));

	}
}
