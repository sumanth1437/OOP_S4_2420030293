package Operators;

public class CompoundOperators {
	public static void main(String[] args) {
		int x = 10;
        x += 5;   
        System.out.println("After += : " + x);
        x -= 3;  
        System.out.println("After -= : " + x);
        x *= 2;   
        System.out.println("After *= : " + x);
        x /= 4;  
        System.out.println("After /= : " + x);
        x %= 4;   
        System.out.println("After %= : " + x);
    }
}
